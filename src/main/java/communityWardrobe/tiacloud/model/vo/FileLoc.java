package communityWardrobe.tiacloud.model.vo;

public class FileLoc {
	
	private Double x;
	private Double y;
	
	public FileLoc() {
		super();
	}
	public FileLoc(Double x, Double y) {
		super();
		this.x = x;
		this.y = y;
	}
	public Double getX() {
		return x;
	}
	public void setX(Double x) {
		this.x = x;
	}
	public Double getY() {
		return y;
	}
	public void setY(Double y) {
		this.y = y;
	}	

}
